# tuple
# we can create a tuple using parenthesis and tuple ()
# In tuple we maintain insertion order
# tuple is immutable
t1 = 10
print(t1, type(t1))

t2 = 10,
print(t2, type(t2))

t3 = (10, 20, 30, 40)
print(t3, type(t3))

t4 = 10, 20, 30, 40
print(t4, type(t4))  # both t3 and t4 statements are same

t5 = tuple([10, 20, 30, 40])
print(t5, type(t5))
